#include "libft.h"

int		ft_wordcount(const char *s, char c)
{
	size_t n;
	size_t i;

	n = 0;
	i = 0;
	while (s[n])
	{
		while (s[n] == c && s[n] != '\0')
			n++;
		if (s[n] != c && s[n] != '\0')
			i++;
		while (s[n] != c && s[n] != '\0')
			n++;
	}
	return ((int)i);
}
