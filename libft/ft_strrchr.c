#include "libft.h"

char		*ft_strrchr(const char *s1, int n)
{
	int i;

	i = 0;
	while (s1[i] != '\0')
		i++;
	while (i >= 0)
	{
		if (s1[i] == (char)n)
			return ((char *)s1 + i);
		i--;
	}
	return (NULL);
}
