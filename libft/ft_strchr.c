#include "libft.h"

char		*ft_strchr(const char *s1, int s2)
{
	int i;

	i = 0;
	while (s1[i] != '\0')
	{
		if (s1[i] == (char)s2)
		{
			return ((char *)&s1[i]);
		}
		i++;
	}
	if (s1[i] == (char)s2)
	{
		return ((char *)&s1[i]);
	}
	return (NULL);
}
